    private void btnGuessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuessActionPerformed
        switch (counter) {
            case 0:
                play(btnRow1Char1, btnRow1Char2, btnRow1Char3, btnRow1Char4, btnRow1Char5, txtGuess.getText());
                break;
            case 1:
                play(btnRow2Char1, btnRow2Char2, btnRow2Char3, btnRow2Char4, btnRow2Char5, txtGuess.getText());
                break;
            case 2:
                play(btnRow3Char1, btnRow3Char2, btnRow3Char3, btnRow3Char4, btnRow3Char5, txtGuess.getText());
                break;
            case 3:
                play(btnRow4Char1, btnRow4Char2, btnRow4Char3, btnRow4Char4, btnRow4Char5, txtGuess.getText());
                break;
            case 4:
                play(btnRow5Char1, btnRow5Char2, btnRow5Char3, btnRow5Char4, btnRow5Char5, txtGuess.getText());
                break;
            default:
                txtTitle.setText("Out of turns!");
                break;
        }
    }//GEN-LAST:event_btnGuessActionPerformed

