    public void play(JButton btn1, JButton btn2, JButton btn3, JButton btn4, JButton btn5, String str){
        //Uses class to get array values
        int[][] array = word.guesser(txtGuess.getText());
        //Setting new colors
        Color clr1 = new Color(array[0][0], array[0][1], array[0][2]);
        Color clr2 = new Color(array[1][0], array[1][1], array[1][2]);
        Color clr3 = new Color(array[2][0], array[2][1], array[2][2]);
        Color clr4 = new Color(array[3][0], array[3][1], array[3][2]);
        Color clr5 = new Color(array[4][0], array[4][1], array[4][2]);
        //Giving array rgb values to buttons to se the color
        btn1.setBackground(clr1);
        btn2.setBackground(clr2);
        btn3.setBackground(clr3);
        btn4.setBackground(clr4);
        btn5.setBackground(clr5);
        //Settign text for buttons
        btn1.setText(str.substring(0,1));
        btn2.setText(str.substring(1,2));
        btn3.setText(str.substring(2,3));
        btn4.setText(str.substring(3,4));
        btn5.setText(str.substring(4));
        //Increase counter variable
        counter++;
        //Reset txt box
        txtGuess.setText("");
    }
    
   
    
    
